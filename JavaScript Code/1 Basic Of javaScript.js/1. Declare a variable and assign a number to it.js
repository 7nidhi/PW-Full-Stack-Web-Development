// Declare a variable and assign number 

let x=10;            //let can be reassigned but not redeclared in same scope. Block scope
var y=20;            //var can be reassigned and redeclared. Global scope
const z=30;          // const can not be reassigned and not update. Block scope.

console.log(`let values is: ${x}, var value is: ${y}, const value is: ${z}`);
