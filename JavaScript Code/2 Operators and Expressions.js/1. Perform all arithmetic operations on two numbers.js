//Perform all arithmetic operations on two numbers.
 function add(x,y){
    return x+y;
 }

 function sub(x,y){
    return x-y;
 }

 function multiply(x,y){
    return x*y;
 }

 function div(x,y){
    return x/y;
 }

 function modulus(x,y){
    return x%y;
 }

 function increment(x){
    return ++x;
 }

 function decrement(x){
    return --x;
 }
 function exponent(x,y){
    return x**y;
 }


 console.log(add(5,6));
 console.log(sub(7,6));
 console.log(multiply(10,7));
 console.log(div(25,5));
 console.log(increment(5));
 console.log(decrement(5));
 console.log(modulus(7,3));
 console.log(exponent(5,3));